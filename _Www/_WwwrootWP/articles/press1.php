Октябрь 2004. Пресс релиз выхода новой версии WireChanger
<br><br>
Вышла новая версия полноценного менеджера десктопа - <b>WireChanger</b> 2.8. Программа позволяет
полностью настроить Ваш десктоп в соответствии с Вашими вкусами, добавив к нему необходимые
ингридиенты в виде заметок, календаря, актуального прогноза погоды, новостей и др.
Наверно каждый согласится, что запускать программы с ярлыков на десктопе и сохранять
документы прямо на десктоп довольно удобно - настроенные "под себя" ярлычки всегда под
рукой, не нужно каждый раз вспоминать "Куда я положил этот файл?". Уникальность расположения
десктопа в системе позволяет экономить достаточно много времени на выполнение рутинных
операций. Но используется ли при этом его потенциал на полную мощность? Как показали
исследования - нет, не используется. И речь здесь идет о пространстве "за иконками",
известном как "Обои" Windows. Хотя система и предоставляет возможность устанавливать туда
свою картинку, но этим все и ограничивается. Однако это место можно использовать для
хранения информации которая должна быть всегда перед глазами, напоминая о вещах которые
"нужно не забыть", для показа собранной с просторов интернета информации по интересующей Вас
тематике и многого другого.
<br><br>
Но отсутствие встроенных средств легко исправить воспользовавшись правильным инструментов.
В качестве одной из альтернатив предлагается программа WireChanger, которая недавно
обновилась до версии 2.8. Она позволяет повысить эффективность работы в Windows располагая
