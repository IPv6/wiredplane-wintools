позволяет добавить к фону часы из обширной online коллекции - одного взгляда на десктоп
будет достаточно будет чтобы понять что Вам нужно делать именно сейчас. Программа также
может добавить к картинке на десктопе случайную цитату какого-либо известного человека и
прогноз погоды на завтра. И конечно, программа позволяет менять фоновое изображение - при
этом не ограничиваясь простой заменой картинки на другую. Рисунок может быть художественно
изменен с помощью большой встроенной библиотеки эффектов.
